# Create a list
some_list = ['man', 'bear', 'pig']

# Unpack the list
man, bear, pig = some_list

'''
The statement above is equivalent to:

man = some_list[0]
bear = some_list[1]
pig = some_list[2]
'''

# Show that the variables represent the values of the original list
print(man, bear, pig)
print(some_list)